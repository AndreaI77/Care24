                    <p>
                        Una cookie o galleta informática es un pequeño archivo de información que se guarda en su navegador cada
                        vez que visita nuestra página web.
                        <br />
                        La utilidad de las cookies es guardar el historial de su actividad en nuestra página web, de manera que,
