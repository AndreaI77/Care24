                            medición de la actividad de los sitios web,
                            aplicación o plataforma y para la elaboración de perfiles de navegación de los usuarios de dichos
                            sitios,
                            aplicaciones y plataformas, con el fin de introducir mejoras en función del análisis de los datos de
                            uso que hacen los usuarios del servicio.
                        </p>
                        <li>Cookies publicitarias:</li>
                        <p>
                            Son aquéllas que permiten la gestión, de la forma más eficaz posible, de los espacios publicitarios
                            que, en su caso, el responsable haya incluido en una página web,
                            aplicación o plataforma desde la que presta el servicio solicitado en base a criterios como el
