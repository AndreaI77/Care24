                            plataforma o aplicación y la utilización de las diferentes opciones o servicios que en ella existan
                            como, por ejemplo,
                            controlar el tráfico y la comunicación de datos, identificar la sesión, acceder a partes de acceso
                            restringido,
                            recordar los elementos que integran un pedido, realizar el proceso de compra de un pedido,
                            realizar la solicitud de inscripción o participación en un evento, utilizar elementos de seguridad
                            durante la navegación,
                            almacenar contenidos para la difusión de videos o sonido o compartir contenidos a través de redes
                            sociales.
                        </p>
                        <li>Cookies de personalización:</li>
                        <p>
                            Son aquéllas que permiten al usuario acceder al servicio con algunas características de carácter
                            general predefinidas
                            en función de una serie de criterios en el terminal del usuario como por ejemplo serian el idioma,
                            el tipo de navegador a través del cual accede al servicio, la configuración regional desde donde
                            accede al servicio, etc.
                        </p>
