                        ésta pueda identificarle y configurar el contenido de la misma en base a sus hábitos de navegación,
                        identidad y preferencias.
                        <br />
                        Una cookie es inofensiva, no contiene código malicioso o malintencionado (ej. virus, troyanos, gusanos,
                        etc.) que pueda dañar su terminal
                        (ordenador, smartphone, tableta, etc.), pero sí tiene cierto impacto sobre su derecho a la protección de
                        sus datos,
                        <br /> pues recoge determinada información concerniente a su persona (hábitos de navegación, identidad,
                        preferencias, etc.).
                    </p>
                    <li><strong>¿Qué información guarda una cookie?</strong></li>
                    <p>
                        Las cookies no suelen recoger categorías especiales de datos personales (datos sensibles).
                        Los datos que guardan son de carácter técnico, preferencias personales, personalización de contenidos,
                        etc.
                    </p>
                    <li><strong>¿Qué tipo de cookies existen?</strong></li>
                    <p>
                        Con carácter general, existen cinco tipos de cookies:
                    <ol>
